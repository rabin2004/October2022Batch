package oOPSconcept;

public class InterfaceAbstractionConcept extends InterfaceImplementationUnderstanding{

	@Override
	public void accountInfo() {
		System.out.println("Account information");
		
	}

	@Override
	public void cancelAccount() {
		System.out.println("Account cancelled");
		
	}

	@Override
	public void rentAppliance() {
		System.out.println("Rent appliance completed");
		
	}

}
